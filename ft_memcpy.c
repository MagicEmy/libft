#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	const char	*s;
	char		*d;

	s = src;
	d = dst;
	if (src == dst)
		return (NULL);
	while (n--)
	{
		*d++ = *s++;
	}
	return (dst);
}
